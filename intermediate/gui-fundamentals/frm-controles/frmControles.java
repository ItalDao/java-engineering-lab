    private void btnProcesarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnProcesarMouseClicked
        
        txtResultado1.setText("");
        txtResultado2.setText("");
        
        // Se evalua el género
        if(rdbMasculino.isSelected()==true){
            txtResultado1.setText("Masculino");
        }else if(rdbFemenino.isSelected()==true){
            txtResultado1.setText("Femenino");
        }else{
            txtResultado1.setText("No se tiene selección");
        }
        
        //Se evalua la carrera profesional
        if(chkBasededatos.isSelected()==true){
            txtResultado2.setText("Administrador de la Base de datos");
        }
        if(chkSistemas.isSelected()==true){
            txtResultado2.setText(txtResultado2.getText()+" - " +"Desarrollador de Sistemas");
        }
        if(chkProyectos.isSelected()==true){
            txtResultado2.setText(txtResultado2.getText()+" - " +"Analista de Proyectos");
        }
        
    }//GEN-LAST:event_btnProcesarMouseClicked

